export interface DetectedProjectFacts {
  root: string;
  packageManager?: string | null;
  existingWorkflowPath?: string | null;
  githubRepository?: {
    owner: string;
    repo: string;
    remote: string;
  } | null;
}

export interface InitUserChoices {
  dryRun: boolean;
  selections: readonly string[];
  portableSkillIds?: readonly string[];
  noPortableSkills?: boolean;
  linearProjectSlug?: string | null;
  githubOwner?: string | null;
  githubRepo?: string | null;
}

export type FilePlanAction = 'create' | 'overwrite' | 'skip';
export type FilePlanOverwriteStatus = 'absent' | 'exists' | 'not_checked';

export interface WorkflowFilePlanEntry {
  path: string;
  action: FilePlanAction;
  overwriteStatus: FilePlanOverwriteStatus;
  wouldWrite: boolean;
  mode?: number;
  content?: string;
  contentPreview: string;
  validationNotes: string[];
  requiresOverwriteApproval: boolean;
}

export interface WorkflowMaterializationPlan {
  dryRun: boolean;
  selections: readonly string[];
  portableSkills: PortableSkillSelection;
  selectedPacks: string[];
  bundleProvenance: Array<{ bundle: string; packs: string[] }>;
  detectedProjectFacts: DetectedProjectFacts;
  files: WorkflowFilePlanEntry[];
  validation: ValidationResult;
}

export interface WorkflowMaterializerOptions {
  resolution: ProfileResolution;
  projectFacts: DetectedProjectFacts;
  choices: InitUserChoices;
  clock?: () => Date;
}

const WORKFLOW_STATE_GITIGNORE = ['*', '!.gitignore', ''].join('\n');
const WORKTREE_INCLUDE = ['.env', '.env.local', '.codex/**', ''].join('\n');
const GENERATED_VALIDATION_ENV = {
  LINEAR_API_KEY: 'generated-validation-placeholder',
  GITHUB_TOKEN: 'generated-validation-placeholder'
};

export function materializeWorkflowPlan(options: WorkflowMaterializerOptions): WorkflowMaterializationPlan {
  if (options.resolution.errors.length > 0) {
    throw new Error(options.resolution.errors.join('\n'));
  }
  if (options.resolution.protectedBindings.length > 0) {
    throw new Error('Protected workflow bindings cannot be generated by init. Choose workflow:solo-local or workflow:team-review.');
  }
  const hostedInputErrors = validateHostedTrackerInputs(options);
  if (hostedInputErrors.length > 0) {
    throw new Error(hostedInputErrors.join('\n'));
  }

  const portableSkills = resolveInitPortableSkillSelection(options.choices);
  const workflowContent = renderGeneratedWorkflow(options);
  const workflowPath = path.join(options.projectFacts.root, 'WORKFLOW.md');
  const workflowValidation = validateWorkflowContent(workflowContent, workflowPath);
  const files: WorkflowFilePlanEntry[] = [
    buildFilePlanEntry({
      root: options.projectFacts.root,
      relativePath: 'WORKFLOW.md',
      content: workflowContent,
      notes: workflowValidation.ok
        ? ['generated workflow parsed and validated successfully']
        : [`generated workflow validation failed: ${workflowValidation.message}`]
    }),
    ...buildOptionalSupportFiles(options),
    buildFilePlanEntry({
      root: options.projectFacts.root,
      relativePath: path.join('.symphony', 'system', '.gitignore'),
      content: WORKFLOW_STATE_GITIGNORE,
      notes: ['keeps generated local runtime state out of version control']
    }),
    buildGitignorePlanEntry({
      root: options.projectFacts.root,
      ignorePath: '.gitignore',
      entry: '.symphony/system/'
    }),
    ...buildPortableSkillFiles(options, portableSkills)
  ];

  return {
    dryRun: options.choices.dryRun,
    selections: options.choices.selections,
    portableSkills,
    selectedPacks: options.resolution.packs.map((pack) => pack.id),
    bundleProvenance: options.resolution.expandedBundles.map((expansion) => ({
      bundle: expansion.bundle.id,
      packs: [...expansion.packs]
    })),
    detectedProjectFacts: options.projectFacts,
    files,
    validation: workflowValidation
  };
}

function resolveInitPortableSkillSelection(choices: InitUserChoices): PortableSkillSelection {
  if (choices.noPortableSkills) {
    return resolvePortableSkillSelection([]);
  }
  return resolvePortableSkillSelection(choices.portableSkillIds);
}

function buildPortableSkillFiles(
  options: WorkflowMaterializerOptions,
  portableSkills: PortableSkillSelection
): WorkflowFilePlanEntry[] {
  if (portableSkills.selectedSkillIds.length === 0) {
    return [];
  }

  const assets = resolvePortableSkillAssetSet(portableSkills.selectedSkillIds);
  const files: WorkflowFilePlanEntry[] = [];
  for (const skill of assets.selectedSkills) {
    validatePortableSkillAssetContainment(assets.assetRoot, skill.absoluteSourceDirectory, skill.sourceDirectory);
    validatePortableSkillAssetContainment(assets.assetRoot, skill.absoluteTemplatePath, path.join(skill.sourceDirectory, 'SKILL.md'));
    files.push(
      buildSkillFilePlanEntry({
        root: options.projectFacts.root,
        skillDirectory: skill.destinationDirectory,
        relativePath: path.join(skill.destinationDirectory, 'SKILL.md'),
        content: fs.readFileSync(skill.absoluteTemplatePath, 'utf8'),
        mode: fs.statSync(skill.absoluteTemplatePath).mode & 0o777,
        notes: [`plans project-local portable skill template from ${skill.sourceDirectory}`]
      })
    );
    for (const helper of skill.helperScripts) {
      validatePortableSkillAssetContainment(assets.assetRoot, helper.absolutePath, helper.path);
      files.push(
        buildSkillFilePlanEntry({
          root: options.projectFacts.root,
          skillDirectory: skill.destinationDirectory,
          relativePath: helper.destinationPath,
          content: fs.readFileSync(helper.absolutePath, 'utf8'),
          mode: fs.statSync(helper.absolutePath).mode & 0o777,
          notes: [`plans ${helper.runtime} helper script for portable skill ${skill.id}`]
        })
      );
    }
  }

  return files;
}

function validateHostedTrackerInputs(options: WorkflowMaterializerOptions): string[] {
  const trackerKind = options.resolution.dimensions.tracker?.name;
  const errors: string[] = [];

  if (trackerKind === 'linear' && !options.choices.linearProjectSlug?.trim()) {
    errors.push('Missing required Linear project slug. Pass --linear-project-slug <slug> before generating tracker:linear workflows.');
  }

  if (trackerKind === 'github') {
    const detected = options.projectFacts.githubRepository;
    const owner = options.choices.githubOwner?.trim() || detected?.owner;
    const repo = options.choices.githubRepo?.trim() || detected?.repo;
    if (!owner) {
      errors.push('Missing required GitHub owner. Pass --github-owner <owner> or configure a GitHub git remote.');
    }
    if (!repo) {
      errors.push('Missing required GitHub repo. Pass --github-repo <repo> or configure a GitHub git remote.');
    }
  }

  return errors;
}

export function materializeWorkflowDryRun(options: WorkflowMaterializerOptions): WorkflowMaterializationPlan {
  return materializeWorkflowPlan({
    ...options,
    choices: { ...options.choices, dryRun: true }
  });
}

export function renderWorkflowFilePlan(plan: WorkflowMaterializationPlan): string {
  const lines = [
    `Symphony init ${plan.dryRun ? 'dry-run' : 'write'} file plan`,
    '',
    `Dry run: ${plan.dryRun ? 'yes' : 'no'}`,
    `Selections: ${plan.selections.join(', ') || '(none)'}`,
    `Selected packs: ${plan.selectedPacks.join(', ')}`,
    `Portable skills: ${plan.portableSkills.selectedSkillIds.join(', ') || '(none)'}`,
    `Portable opt-in skills: ${plan.portableSkills.optInSkillIds.join(', ') || '(none)'}`,
    `Bundle provenance: ${formatBundleProvenance(plan)}`,
    `Project root: ${plan.detectedProjectFacts.root}`,
    `Writes performed: ${plan.dryRun ? 'no' : 'pending'}`,
    `Validation: ${plan.validation.ok ? 'ok' : `failed (${plan.validation.error_code})`}`,
    '',
    'Files:'
  ];

  for (const [index, file] of plan.files.entries()) {
    lines.push(
      `  ${index + 1}. ${file.path}`,
      `     action: ${file.action}`,
      `     overwrite: ${file.overwriteStatus}`,
      `     would_write: ${file.wouldWrite ? 'yes' : 'no'}`,
      `     overwrite_approval_required: ${file.requiresOverwriteApproval ? 'yes' : 'no'}`,
      `     validation_notes: ${file.validationNotes.join('; ') || 'none'}`,
      '     content:',
      indent(file.content ?? file.contentPreview, '       ')
    );
  }

  return `${lines.join('\n')}\n`;
}

function renderGeneratedWorkflow(options: WorkflowMaterializerOptions): string {
  const dimensions = options.resolution.dimensions;
  const tracker = requirePack(dimensions.tracker, 'tracker');
  const workspace = requirePack(dimensions.workspace, 'workspace');
  const toolchain = requirePack(dimensions.toolchain, 'toolchain');
  const workflow = requirePack(dimensions.workflow, 'workflow');
  const trackerKind = tracker.name;
  const workspaceType = workspace.name;
  const profileName = workflow.name;
  const activeStates = getActiveStates(trackerKind, profileName);
  const handoffStates = getHandoffStates(trackerKind, profileName);
  const freshDispatchStates = getFreshDispatchStates(trackerKind, profileName);
  const terminalStates = getTerminalStates(trackerKind, profileName);
  const bundle = options.resolution.expandedBundles.map((expansion) => expansion.bundle.id).join(',');
  const packs = options.resolution.packs.map((pack) => pack.id).join(',');
  const packageManager = options.projectFacts.packageManager ?? 'unknown';
  const trackerConfig = buildTrackerConfig(options, trackerKind);
  const workspaceConfig = buildWorkspaceConfig(workspaceType);
  const toolchainConfig = buildToolchainConfig(toolchain.name, packageManager);
  const workspaceBaseRef = workspaceType === 'clone' ? 'main' : 'origin/main';
  const codexSandbox = workspaceType === 'worktree' ? 'danger-full-access' : 'read-only';

  return [
    '---',
    'symphony:',
    '  generated_profile:',
    `    profile: ${yamlString(profileName)}`,
    `    bundle: ${yamlString(bundle || 'explicit-packs')}`,
    '    packs:',
    ...options.resolution.packs.map((pack) => `      - ${yamlString(pack.id)}`),
    '    generated_by: "symphony init"',
    'tracker:',
    `  kind: ${yamlString(trackerKind)}`,
    ...trackerConfig,
    `  active_states: ${yamlList(activeStates)}`,
    `  terminal_states: ${yamlList(terminalStates)}`,
    ...optionalTrackerStateLines(handoffStates, freshDispatchStates),
    'workspace:',
    ...workspaceConfig,
    '  provisioner:',
    `    type: ${yamlString(workspaceType)}`,
    ...(workspaceType === 'worktree' || workspaceType === 'clone' ? ['    repo_root: "."'] : []),
    `    base_ref: ${yamlString(workspaceBaseRef)}`,
    '    branch_template: "feature/{{ issue.identifier }}"',
    '    teardown_mode: "remove_worktree"',
    '    allow_dirty_repo: false',
    '    fallback_to_clone_on_worktree_failure: false',
    'toolchain:',
    `  kind: ${yamlString(toolchain.name)}`,
    ...toolchainConfig,
    'hooks:',
    ...buildHookConfig(toolchain.name, packageManager),
    'codex:',
    '  command: codex app-server',
    `  thread_sandbox: ${yamlString(codexSandbox)}`,
    `  turn_sandbox_policy: ${yamlString(codexSandbox)}`,
    '  approval_policy: never',
    '  user_input_policy: fail_attempt',
    'persistence:',
    '  enabled: true',
    'validation:',
    '  ui_evidence_profile: baseline',
    '---',
    `<!-- symphony-generated-profile: profile=${profileName}; bundle=${bundle || 'explicit-packs'}; packs=${packs}; bundle_provenance=${formatBundleProvenanceForComment(options.resolution)}; -->`,
    '',
    '# Symphony Workflow',
    '',
    `Generated by \`symphony init\` for the ${profileName} workflow style.`,
    '',
    '## Selected Packs',
    '',
    ...options.resolution.packs.map((pack) => `- ${pack.id}: ${pack.summary}`),
    '',
    '## Detected Project Facts',
    '',
    '- Project root: .',
    `- Package manager: ${packageManager}`,
    `- GitHub repository: ${formatDetectedGitHubRepository(options)}`,
    '',
    '## Generated Inputs To Review',
    '',
    ...buildGeneratedInputLines(options, trackerKind),
    '',
    '## Toolchain Commands',
    '',
    ...buildToolchainCommandLines(toolchain.name, packageManager),
    '',
    '## Operator Contract',
    '',
    '- Work one issue at a time and keep progress evidence in the tracker.',
    '- Run the validation commands that match the repository before sharing results.',
    '- For dry-run initialization, review this generated file plan before enabling any write path.',
    '- Keep generated runtime state under .symphony/system/ and out of version control.'
  ].join('\n');
}

function optionalTrackerStateLines(
  handoffStates: readonly string[],
  freshDispatchStates: readonly string[]
): string[] {
  if (handoffStates.length === 0 && freshDispatchStates.length === 0) {
    return [];
  }
  return [`  handoff_states: ${yamlList(handoffStates)}`, `  fresh_dispatch_states: ${yamlList(freshDispatchStates)}`];
}

export function validateWorkflowContent(content: string, workflowPath: string): ValidationResult {
  try {
    const workflowDefinition = new WorkflowLoader().parse(content);
    const provenanceValidation = validateWorkflowGeneratedProfileProvenance({
      config: workflowDefinition.config,
      workflowText: content
    });
    if (!provenanceValidation.ok) {
      return {
        ok: false,
        error_code: 'invalid_generated_profile_provenance',
        message: provenanceValidation.message,
        at: '2026-05-25T00:00:00.000Z'
      };
    }
    const effectiveConfig = new ConfigResolver({
      env: GENERATED_VALIDATION_ENV,
      homedir: () => path.dirname(workflowPath)
    }).resolve(workflowDefinition, { workflowPath });
    return new ConfigValidator({ clock: () => new Date('2026-05-25T00:00:00.000Z') }).validate(effectiveConfig);
  } catch (error) {
    return {
      ok: false,
      error_code: 'workflow_parse_error',
      message: error instanceof Error ? error.message : String(error),
      at: '2026-05-25T00:00:00.000Z'
    };
  }
}

function getActiveStates(trackerKind: string, profileName: string): string[] {
  if (trackerKind === 'github') {
    return ['Open'];
  }
  return profileName === 'team-review' ? ['Todo', 'In Progress', 'Agent Review'] : ['Todo', 'In Progress'];
}

function getHandoffStates(trackerKind: string, profileName: string): string[] {
  return trackerKind !== 'github' && profileName === 'team-review' ? ['Agent Review'] : [];
}

function getFreshDispatchStates(trackerKind: string, profileName: string): string[] {
  return trackerKind !== 'github' && profileName === 'team-review' ? ['Agent Review'] : [];
}

function getTerminalStates(trackerKind: string, profileName: string): string[] {
  if (trackerKind === 'github') {
    return ['Closed'];
  }
  return profileName === 'team-review' ? ['Done', 'Canceled'] : ['Done'];
}

function buildOptionalSupportFiles(options: WorkflowMaterializerOptions): WorkflowFilePlanEntry[] {
  const files: WorkflowFilePlanEntry[] = [];
  const trackerKind = options.resolution.dimensions.tracker?.name;
  const workspaceType = options.resolution.dimensions.workspace?.name;

  if (trackerKind === 'linear' || trackerKind === 'github') {
    files.push(
      buildFilePlanEntry({
        root: options.projectFacts.root,
        relativePath: '.env.example',
        content: buildEnvExample(trackerKind),
        notes: [`documents required ${trackerKind} credential variables without secrets`]
      })
    );
  }

  if (workspaceType === 'worktree') {
    files.push(
      buildFilePlanEntry({
        root: options.projectFacts.root,
        relativePath: '.worktreeinclude',
        content: WORKTREE_INCLUDE,
        notes: ['declares ignored local files copied into managed worktrees']
      })
    );
  }

  return files;
}

function buildEnvExample(trackerKind: string): string {
  if (trackerKind === 'linear') {
    return ['# Required by generated tracker:linear workflows.', '# Copy to .env or export in your shell; never commit real secrets.', 'LINEAR_API_KEY=', ''].join('\n');
  }

  return ['# Required by generated tracker:github workflows.', '# Copy to .env or export in your shell; never commit real secrets.', 'GITHUB_TOKEN=', ''].join('\n');
}

function buildTrackerConfig(options: WorkflowMaterializerOptions, trackerKind: string): string[] {
  if (trackerKind === 'linear') {
    return [
      '  endpoint: "https://api.linear.app/graphql"',
      '  api_key: "$LINEAR_API_KEY"',
      `  project_slug: ${yamlString(options.choices.linearProjectSlug?.trim() || 'LINEAR_PROJECT_SLUG')}`,
      '  github_linking:',
      '    mode: "warn"'
    ];
  }

  if (trackerKind === 'github') {
    const detected = options.projectFacts.githubRepository;
    const owner = options.choices.githubOwner?.trim() || detected?.owner || 'GITHUB_OWNER';
    const repo = options.choices.githubRepo?.trim() || detected?.repo || 'GITHUB_REPO';
    return [
      '  endpoint: "https://api.github.com/graphql"',
      '  api_key: "$GITHUB_TOKEN"',
      `  owner: ${yamlString(owner)}`,
      `  repo: ${yamlString(repo)}`,
      '  github_linking:',
      '    mode: "off"'
    ];
  }

  return ['  endpoint: "memory://local"', '  api_key: ""', '  github_linking:', '    mode: "off"'];
}

function buildWorkspaceConfig(workspaceType: string): string[] {
  const copyIgnoredEnabled = workspaceType === 'worktree';
  return [
    '  copy_ignored:',
    `    enabled: ${copyIgnoredEnabled ? 'true' : 'false'}`,
    '    include_file: ".worktreeinclude"',
    '    from: "primary_worktree"',
    '    conflict_policy: "skip"',
    '    require_gitignored: true',
    '    max_files: 10000',
    '    max_total_bytes: 5368709120',
    '    allow_patterns: []',
    '    deny_patterns: []'
  ];
}

function buildToolchainConfig(toolchainName: string, packageManager: string): string[] {
  if (toolchainName !== 'node') {
    return ['  setup_command: ""', '  validation_command: "git diff --check"'];
  }

  return [
    `  package_manager: ${yamlString(packageManager === 'unknown' ? 'npm' : packageManager)}`,
    `  setup_command: ${yamlString(resolveNodeInstallCommand(packageManager))}`,
    `  validation_command: ${yamlString(resolveNodeTestCommand(packageManager))}`
  ];
}

function buildHookConfig(toolchainName: string, packageManager: string): string[] {
  if (toolchainName !== 'node') {
    return ['  after_create: ""', '  before_run: ""', '  after_run: ""', '  before_remove: ""', '  timeout_ms: 60000'];
  }

  return [
    `  after_create: ${yamlString(resolveNodeInstallCommand(packageManager))}`,
    '  before_run: ""',
    '  after_run: ""',
    '  before_remove: ""',
    '  timeout_ms: 60000'
  ];
}

function buildGeneratedInputLines(options: WorkflowMaterializerOptions, trackerKind: string): string[] {
  if (trackerKind === 'linear') {
    return [
      `- Linear API key: env var LINEAR_API_KEY (see .env.example)`,
      `- Linear project slug: ${options.choices.linearProjectSlug?.trim() || 'LINEAR_PROJECT_SLUG'}`
    ];
  }

  if (trackerKind === 'github') {
    const detected = options.projectFacts.githubRepository;
    const owner = options.choices.githubOwner?.trim() || detected?.owner || 'GITHUB_OWNER';
    const repo = options.choices.githubRepo?.trim() || detected?.repo || 'GITHUB_REPO';
    return [
      '- GitHub token: env var GITHUB_TOKEN (see .env.example)',
      `- GitHub owner: ${owner}${detected && owner === detected.owner ? ' (detected from git remote)' : ''}`,
      `- GitHub repo: ${repo}${detected && repo === detected.repo ? ' (detected from git remote)' : ''}`
    ];
  }

  return ['- Memory tracker: no hosted tracker credentials required.'];
}

function buildToolchainCommandLines(toolchainName: string, packageManager: string): string[] {
  if (toolchainName !== 'node') {
    return ['- Setup: none', '- Validation: git diff --check'];
  }

  return [
    `- Package manager: ${packageManager === 'unknown' ? 'npm (default)' : packageManager}`,
    `- Setup: ${resolveNodeInstallCommand(packageManager)}`,
    `- Validation: ${resolveNodeTestCommand(packageManager)}`
  ];
}

function resolveNodeInstallCommand(packageManager: string): string {
  if (packageManager === 'pnpm') {
    return 'pnpm install';
  }
  if (packageManager === 'yarn') {
    return 'yarn install';
  }
  return 'npm install';
}

function resolveNodeTestCommand(packageManager: string): string {
  if (packageManager === 'pnpm') {
    return 'pnpm test';
  }
  if (packageManager === 'yarn') {
    return 'yarn test';
  }
  return 'npm test';
}

function formatDetectedGitHubRepository(options: WorkflowMaterializerOptions): string {
  const detected = options.projectFacts.githubRepository;
  if (!detected) {
    return 'not detected';
  }
  return `${detected.owner}/${detected.repo} (${detected.remote})`;
}

function buildFilePlanEntry(params: {
  root: string;
  relativePath: string;
  content: string;
  mode?: number;
  notes: string[];
}): WorkflowFilePlanEntry {
  const absolutePath = path.join(params.root, params.relativePath);
  assertPathInside(params.root, absolutePath, `init destination ${params.relativePath}`);
  const exists = fs.existsSync(absolutePath);
  const existingContent = exists ? fs.readFileSync(absolutePath, 'utf8') : null;
  const action: FilePlanAction = exists ? (existingContent === params.content ? 'skip' : 'overwrite') : 'create';
  return {
    path: params.relativePath,
    action,
    overwriteStatus: exists ? 'exists' : 'absent',
    wouldWrite: action !== 'skip',
    mode: params.mode,
    content: params.content,
    contentPreview: preview(params.content),
    validationNotes: action === 'skip' ? [...params.notes, 'existing content already matches generated content'] : params.notes,
    requiresOverwriteApproval: action === 'overwrite'
  };
}

function buildSkillFilePlanEntry(params: {
  root: string;
  skillDirectory: string;
  relativePath: string;
  content: string;
  mode: number;
  notes: string[];
}): WorkflowFilePlanEntry {
  const projectRoot = path.resolve(params.root);
  const skillRoot = path.resolve(projectRoot, params.skillDirectory);
  const destinationPath = path.resolve(projectRoot, params.relativePath);
  assertPathInside(projectRoot, skillRoot, `portable skill destination ${params.skillDirectory}`);
  assertPathInside(projectRoot, destinationPath, `portable skill destination ${params.relativePath}`);
  assertPathInside(skillRoot, destinationPath, `portable skill destination ${params.relativePath}`);
  return buildFilePlanEntry(params);
}

function validatePortableSkillAssetContainment(assetRoot: string, absolutePath: string, label: string): void {
  assertPathInside(path.resolve(assetRoot), path.resolve(absolutePath), `portable skill source ${label}`);
}

function assertPathInside(root: string, candidate: string, label: string): void {
  const relative = path.relative(path.resolve(root), path.resolve(candidate));
  if (relative === '' || (!relative.startsWith('..') && !path.isAbsolute(relative))) {
    return;
  }
  throw new Error(`${label} escapes the allowed root ${root}. Refusing to materialize portable skill assets.`);
}

function buildGitignorePlanEntry(params: { root: string; ignorePath: string; entry: string }): WorkflowFilePlanEntry {
  const absolutePath = path.join(params.root, params.ignorePath);
  const exists = fs.existsSync(absolutePath);
  const existingContent = exists ? fs.readFileSync(absolutePath, 'utf8') : '';
  const lines = existingContent.length > 0 ? existingContent.split('\n') : [];
  const hasEntry = lines.some((line) => line.trim() === params.entry);
  const content = hasEntry ? existingContent : appendGitignoreEntry(existingContent, params.entry);
  const action: FilePlanAction = hasEntry ? 'skip' : exists ? 'overwrite' : 'create';

  return {
    path: params.ignorePath,
    action,
    overwriteStatus: exists ? 'exists' : 'absent',
    wouldWrite: action !== 'skip',
    content,
    contentPreview: preview(content),
    validationNotes: hasEntry
      ? ['root .gitignore already contains .symphony/system/']
      : ['adds .symphony/system/ to keep local runtime state out of version control'],
    requiresOverwriteApproval: false
  };
}

function appendGitignoreEntry(content: string, entry: string): string {
  if (content.length === 0) {
    return `${entry}\n`;
  }
  const separator = content.endsWith('\n') ? '' : '\n';
  return `${content}${separator}${entry}\n`;
}

function requirePack(pack: ProfilePack | undefined, dimension: string): ProfilePack {
  if (!pack) {
    throw new Error(`Missing required ${dimension} pack.`);
  }
  return pack;
}

function preview(content: string): string {
  return content.length <= 400 ? content : `${content.slice(0, 397)}...`;
}

function formatBundleProvenance(plan: WorkflowMaterializationPlan): string {
  if (plan.bundleProvenance.length === 0) {
    return 'explicit-packs';
  }
  return plan.bundleProvenance.map((entry) => `${entry.bundle} -> ${entry.packs.join(', ')}`).join('; ');
}

function formatBundleProvenanceForComment(resolution: ProfileResolution): string {
  if (resolution.expandedBundles.length === 0) {
    return 'explicit-packs';
  }
  return resolution.expandedBundles.map((entry) => `${entry.bundle.id}->${entry.packs.join(',')}`).join('|');
}

function yamlList(values: readonly string[]): string {
  return `[${values.map(yamlString).join(', ')}]`;
}

function yamlString(value: string): string {
  return JSON.stringify(value);
}

function indent(text: string, prefix: string): string {
  return text
    .split('\n')
    .map((line) => `${prefix}${line}`)
    .join('\n');
}
