const fs = require('node:fs');
const path = require('node:path');

const esbuild = require('esbuild');

const root = path.resolve(__dirname, '..');
const checkOnly = process.argv.includes('--check');

const targets = [
  {
    name: 'dashboard',
    entryPoint: path.join(root, 'src/api/dashboard-client/entry.ts'),
    outputFile: path.join(root, 'src/api/dashboard-assets/generated-client.ts'),
    constName: 'GENERATED_DASHBOARD_CLIENT_JS',
    sourceLabel: 'src/api/dashboard-client/entry.ts'
  },
  {
    name: 'lens',
    entryPoint: path.join(root, 'src/api/lens-client/entry.ts'),
    outputFile: path.join(root, 'src/api/dashboard-assets/generated-lens-client.ts'),
    constName: 'GENERATED_LENS_CLIENT_JS',
    sourceLabel: 'src/api/lens-client/entry.ts'
  }
];

let staleCount = 0;
for (const target of targets) {
  const banner = [
    '// Generated by scripts/build-dashboard-client.js.',
    `// Source of truth: ${target.sourceLabel} and its imports.`,
    '// Do not edit this file by hand.',
    ''
  ].join('\n');

  const result = esbuild.buildSync({
    entryPoints: [target.entryPoint],
    bundle: true,
    format: 'iife',
    platform: 'browser',
    target: 'es2020',
    legalComments: 'none',
    write: false
  });

  const bundle = result.outputFiles[0]?.text;
  if (!bundle) {
    throw new Error(`esbuild did not produce a ${target.name} bundle`);
  }

  const source = `${banner}export const ${target.constName} = ${JSON.stringify(bundle)};\n`;

  if (checkOnly) {
    const current = fs.existsSync(target.outputFile) ? fs.readFileSync(target.outputFile, 'utf8') : '';
    if (current !== source) {
      console.error(
        `${target.name} client generated asset is stale. Run \`npm run build:dashboard-client\` and commit ${path.relative(root, target.outputFile)}.`
      );
      staleCount += 1;
    } else {
      console.log(`${target.name} client generated asset is fresh.`);
    }
  } else {
    fs.writeFileSync(target.outputFile, source);
  }
}

if (checkOnly && staleCount > 0) {
  process.exit(1);
}
